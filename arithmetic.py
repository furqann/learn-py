#  Arithmetic Operations in python
print(10 + 3)  # Simple addition Integers
print(10 + 3.32)  # Floating addition
print(10 - 3)  # Integer subtraction
print(10 / 3)  # Will return float number
print(10 * 3)  # Will return float number
print(10 // 3)  # Will return integer number
print(10 % 3)  # Will return the remainder
print(10 ** 3)  # Exponent operator will return

#  Operators precedence
#  () -> ** -> * or / -> + or -
